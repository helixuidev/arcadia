namespace Arcadia.Charts.Components.Dashboard;

/// <summary>
/// The direction of a delta/change indicator.
/// </summary>
public enum DeltaType
{
    /// <summary>Positive change (green, up arrow).</summary>
    Increase,
    /// <summary>Negative change (red, down arrow).</summary>
    Decrease,
    /// <summary>No significant change (gray, right arrow).</summary>
    Neutral
}
